public class VariableDataType {
    public static void main(String[] agrs){
        int i = 10;
        float f = 0.25f;
        double d = 20.5d;
        boolean t = true;
        char c = 'a';
        String s = "Ha Noi";

        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(t);
        System.out.println(c);
        System.out.println(s);

    }
}
